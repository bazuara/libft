void	*ft_memccpy(char *dst, char *src, int c, int n)
{
	int i;

	i = 0;
	while (i < n)
	{
		dst[i] = src[i];
		if (dst[i] == c)
			return (&dst[i + 1]);
		i++;
	}
	return (0);
}
